// भाषा स्विचिंग फंक्शन
function changeLanguage(language) {
    const elements = document.querySelectorAll('[data-lang]');
    elements.forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[language] && translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });
    
    // भाषा प्राथमिकता सेट करें
    document.documentElement.lang = language;
    
    // भाषा विकल्प को स्थानीय स्टोरेज में सहेजें
    localStorage.setItem('selectedLanguage', language);
}

// पेज लोड होने पर भाषा सेट करें
document.addEventListener('DOMContentLoaded', function() {
    // Language switching functionality
    const languageSelect = document.getElementById('languageSelect');
    
    // Load saved language preference
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'hi';
    languageSelect.value = savedLanguage;
    changeLanguage(savedLanguage);

    languageSelect.addEventListener('change', function() {
        const selectedLanguage = this.value;
        localStorage.setItem('selectedLanguage', selectedLanguage);
        changeLanguage(selectedLanguage);
    });

    // स्क्रॉल इवेंट पर हेडर को कॉम्पैक्ट करें
    window.addEventListener('scroll', () => {
        const header = document.querySelector('header');
        if (window.scrollY > 50) {
            header.classList.add('scrolled');
        } else {
            header.classList.remove('scrolled');
        }
    });

    // स्कीम कार्ड्स पर होवर इफेक्ट
    const schemeCards = document.querySelectorAll('.scheme-card');
    schemeCards.forEach(card => {
        card.addEventListener('mouseenter', () => {
            card.style.transform = 'translateY(-10px)';
        });
        
        card.addEventListener('mouseleave', () => {
            card.style.transform = 'translateY(0)';
        });
    });

    // Learn More button functionality
    const learnMoreButtons = document.querySelectorAll('.learn-more');
    learnMoreButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            const category = this.closest('.scheme-card').getAttribute('data-category');
            
            // सरकारी वेबसाइट के लिंक
            const govtLinks = {
                education: 'https://www.education.gov.in/',
                agriculture: 'https://agriculture.gov.in/',
                health: 'https://www.mohfw.gov.in/',
                housing: 'https://pmaymis.gov.in/',
                employment: 'https://www.ncs.gov.in/',
                welfare: 'https://www.nationalwelfareboard.gov.in/'
            };

            // नई विंडो में सरकारी वेबसाइट खोलें
            if (govtLinks[category]) {
                window.open(govtLinks[category], '_blank');
            }
        });
    });

    // Scroll to section if hash exists in URL
    if (window.location.hash) {
        const targetSection = document.querySelector(window.location.hash);
        if (targetSection) {
            targetSection.scrollIntoView({ behavior: 'smooth' });
        }
    }
}); 