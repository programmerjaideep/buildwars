    function findEligibleSchemes(userData) {
        return schemes.filter(scheme => {
            const criteria = scheme.criteria;
            if (criteria.minAge && userData.age < criteria.minAge) return false;
            if (criteria.maxAge && userData.age > criteria.maxAge) return false;
            
            if (criteria.maxIncome && userData.annualIncome > criteria.maxIncome) return false;
            
            if (criteria.gender && !criteria.gender.includes(userData.gender)) return false;
            
            if (criteria.category && !criteria.category.includes(userData.category)) return false;
            
            if (criteria.area && !criteria.area.includes(userData.area)) return false;
            if (criteria.category_type) {
                const hasRequiredType = criteria.category_type.some(type => 
                    userData.category_type.includes(type)
                );
                if (!hasRequiredType) return false;
            }
            
            return true;
        });
    }
